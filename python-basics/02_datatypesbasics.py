# Data types - A data type in Python defines the kind of value a variable can hold and determines what operations can be performed on it.

#int
age = 18
gpa = 8
language = 5
print (f"I'm {age} years old , My gpa is {gpa} , I know {language} languages")

#float
average = 90.5
print (f"My average is {average}")

#String
name = "Renga Nayaki"
print (name)

#Boolean 
IsForSale = True
if IsForSale:
    print ("Available")
else:
    print("Not Available")
