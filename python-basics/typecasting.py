#Type Casting - The input() function reads a line from the user and returns it as a string. You can optionally display a prompt. To work with numbers, you must typecast the input.

# String input
name = input("Enter your name: ")
print("Hello,", name)
# Integer type casting
age = int(input("Enter your age: "))
print("You are", age, "years old.")
age = age + 1
print(age)
# Float type casting
gpa = float (input("Enter your gpa: "))
print(gpa)
